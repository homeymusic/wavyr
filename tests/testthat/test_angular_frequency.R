  expect_equal(PROPERTIES$angular_frequency$unit_latex, "\\frac{\\text{rad}}{\\text{s}}")
  expect_equal(PROPERTIES$angular_frequency$symbol, "\u03C9")
  expect_equal(PROPERTIES$angular_frequency$symbol_latex, "\\omega")
  expect_equal(PROPERTIES$angular_frequency$symbol_expression, 'italic(Ï‰)')
