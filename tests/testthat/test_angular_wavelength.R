test_that("angular_wavelength subclass has correct classes", {
  l_a <- angular_wavelength(10)
  expect_true(inherits(l_a, "angular_wavelength"))
  expect_true(inherits(l_a, "property"))
  expect_equal(l_a$value, 10)
})
